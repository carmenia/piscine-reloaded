#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*des;

	i = 0;
	while (src[i])
		i++;
	des = (char *)malloc(sizeof(*src) * (i + 1));
	i = 0;
	while (src[i])
	{
		des[i] = src[i];
		i++;
	}
	des[i] = '\0';
	return (des);
}
